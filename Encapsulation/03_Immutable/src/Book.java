public class Book {
    private final String name;
    private final String autor;
    private final int countList;
    private final int numberISBN;


    public Book(String name, String autor, int countList, int numberISBN) {
        this.name = name;
        this.autor = autor;
        this.countList = countList;
        this.numberISBN = numberISBN;
    }
}

